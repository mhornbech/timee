#import "AppColors.h"

@implementation AppColors

+ (UIColor *)black
{
    return [UIColor colorWithWhite:0.20 alpha:1.0];
}

+ (UIColor *)grey
{
    return [UIColor colorWithWhite:0.40 alpha:1.0];
}

+ (UIColor *)gold
{
    return [UIColor colorWithRed:243.0/255.0 green:242.0/255.0 blue:190.0/255.0 alpha:1.0];
}

+ (UIColor *)mediumGold
{
    return [UIColor colorWithRed:243.0/255.0 green:242.0/255.0 blue:222.0/255.0 alpha:1.0];
}

+ (UIColor *)darkGold
{
    return [UIColor colorWithRed:232.0/255.0 green:232.0/255.0 blue:180.0/255.0 alpha:1.0];
}

+ (UIColor *)brown
{
    return [UIColor colorWithRed:140.0/255.0 green:140.0/255.0 blue:114.0/255.0 alpha:1.0];
}

+ (UIColor *)beige
{
    return [UIColor colorWithRed:128.0/255.0 green:126.0/255.0 blue:100.0/255.0 alpha:1.0];
}


@end
